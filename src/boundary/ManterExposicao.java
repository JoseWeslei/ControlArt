    public void tbExposicaoLinhaSelecionada(JTable tb){
          
        if (tb.getSelectedRow() != -1){
            tfCodExposicao.setText(exposicoes.get(tb.getSelectedRow()).getCodigoExposicao().toString());
            tfNomeExposicao.setText(exposicoes.get(tb.getSelectedRow()).getNomeExposicao());
            tfDescricaoExposicao.setText(exposicoes.get(tb.getSelectedRow()).getDescricaoExposicao());
            cbPiso.setSelectedItem(exposicoes.get(tb.getSelectedRow()).getPiso());
            cbSetor.setSelectedItem(exposicoes.get(tb.getSelectedRow()).getSetor());
            ftfDataInicioExposicao.setText(exposicoes.get(tb.getSelectedRow()).getDataInicioExposicao());
            ftfDataFimExposicao.setText(exposicoes.get(tb.getSelectedRow()).getDataFimExposicao());
        }
    }
   
    
