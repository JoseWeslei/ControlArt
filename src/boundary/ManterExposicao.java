    public void mostrarExposicao(List<ExposicaoBean> exposicao){
        while(tmExposicao.getRowCount() > 0){
            tmExposicao.removeRow(0);
        }
        if(exposicao.size() > 0){
            String [] campos = new String[] {null, null, null, null};
            for(int i = 0; i < exposicao.size(); i++){
                tmExposicao.addRow(campos);
                tmExposicao.setValueAt(exposicao.get(i).getNomeExposicao(), i, 0);
                tmExposicao.setValueAt(exposicao.get(i).getDataInicioExposicao(), i, 1);
                tmExposicao.setValueAt(exposicao.get(i).getDataFimExposicao(), i, 2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Nenhuma obra encontrada!", "Control + Art", 2);
        }
    }
    public void mostrarExposicaoNome(){
        ExposicaoControl exp = new ExposicaoControl();
        exposicoes = exp.mostrarExposicaoNome(tfNomeExposicao.getText().trim());
        mostrarExposicao(exposicoes);
    }
    
