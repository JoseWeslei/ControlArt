    public void cadastraExposicao(){
        
        ExposicaoBean exposicao = new ExposicaoBean();
        exposicao.setNomeExposicao(tfNomeExposicao.getText().trim());
        exposicao.setDescricaoExposicao(tfDescricaoExposicao.getText().trim());
        exposicao.setDataInicioExposicao(ftfDataInicioExposicao.getText());
        exposicao.setDataFimExposicao(ftfDataFimExposicao.getText());
        exposicao.setPiso(Integer.parseInt(cbPiso.getSelectedItem().toString()));
        exposicao.setSetor(cbSetor.getSelectedItem().toString());
        ExposicaoControl c = new ExposicaoControl();
        c.cadastrarExposicao(exposicao);
        JOptionPane.showMessageDialog(null, "Exposicçao cadastrada com sucesso", "Control + Art", 3);
        limpaTela();
        
        
        
    }
    
    private void limpaTela(){
        while(tmExposicao.getRowCount() > 0){
            tmExposicao.removeRow(0);
        }
        tfCodExposicao.setText("");
        tfNomeExposicao.setText("");
        tfDescricaoExposicao.setText("");
        ftfDataInicioExposicao.setText("");
        ftfDataFimExposicao.setText("");
        cbPiso.setSelectedItem(0);
        cbSetor.setSelectedItem(0);
        
    }
    
